# ESP32 MicroPython - Introduction Script for Testing REPL Basics

# Print a welcome message
print("Welcome to MicroPython on ESP32!")

# Basic math operations
print("Addition: 2 + 3 =", 2 + 3)
print("Subtraction: 7 - 4 =", 7 - 4)
print("Multiplication: 5 * 6 =", 5 * 6)
print("Division: 8 / 2 =", 8 / 2)
print("Integer Division: 7 // 2 =", 7 // 2) 
print("Modulus: 7 % 2 =", 7 % 2)
print("Exponentiation: 2 ** 3 =", 2 ** 3)

# Working with variables
a = 10
b = 20
print("Value of a:", a)
print("Value of b:", b)
print("Sum of a and b:", a + b)

# Simple loop
for i in range(5):
    print("Loop iteration:", i)

# End of script
print("REPL basics test complete!")